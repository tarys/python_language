"""
Даны четыре действительных числа: x1, y1, x2, y2. Напишите функцию 
distance(x1, y1, x2, y2), вычисляющая расстояние между точкой 
(x1,y1) и (x2,y2). Считайте четыре действительных числа и 
выведите результат работы этой функции.

"""



def distance(x1, y1, x2, y2):
    a = abs(y1-y2)
    b = abs(x1 - x2)
    c = (a**2 + b**2)**(1/2) # відстань між точками (x1,y1) і (x2,y2)
    return c

x1=float(input())
y1=float(input())
x2=float(input())
y2=float(input())
print(distance(x1, y1, x2, y2))

