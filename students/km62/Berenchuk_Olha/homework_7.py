#task1------------------------------------------------------------
"""
Найдите индексы первого вхождения максимального элемента. Выведите
два числа: номер строки и номер столбца, в которых стоит 
наибольший элемент в двумерном массиве. Если таких элементов 
несколько, то выводится тот, у которого меньше номер строки, 
а если номера строк равны то тот, у которого меньше номер столбца.

Программа получает на вход размеры массива n и m, затем n строк 
по m чисел в каждой.

"""



list = input().split()  
n = int(list[0])
m = int(list[1])
maxi = 0
maxj = 0
list2 = [[int(j) for j in input().split()] for i in range(n)]
for i in range(0, n):
    for j in range(0, m):
        if list2[i][j] > list2[maxi][maxj]:
            maxi = i
            maxj = j
print(maxi, maxj)

