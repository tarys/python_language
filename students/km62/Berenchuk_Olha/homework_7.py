#task2------------------------------------------------------------
"""
Дано нечетное число n. Создайте двумерный массив из n?n элементов,
заполнив его символами "." (каждый элемент массива является строкой
из одного символа). Затем заполните символами "*" среднюю строку 
массива, средний столбец массива, главную диагональ и побочную 
диагональ. В результате единицы в массиве должны образовывать 
изображение звездочки. Выведите полученный массив на экран, разделяя 
элементы массива пробелами.

"""



n = int(input())
list = [["."] * n for i in range(n)]
s = "*"

for i in range(n):
    list[i][(n - 1)- i] = s
    list[i][i] = s
    list[n // 2][i] = s
    list[i][n // 2] = s
    
for row in list:
    print(' '.join([str(elem) for elem in row]))

