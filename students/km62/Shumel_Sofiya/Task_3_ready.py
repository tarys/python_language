'''По данному натуральном n вычислите сумму 1!+2!+3!+...+n!1!+2!+3!+...+n!. В решении этой задачи можно использовать только один цикл.'''

#defaults
factorial = 1        #int
sum = 0              #int

#input
print ('Task 2: Нахождение суммы факториалов')
number = int(input('Введите натуральное число: '))    #int; число с фиксированной точкой

#main
if number < 0:
    print ('Введено недостимое значение!')
else:
    for i in range (1, number + 1):
        factorial *= i                    #int
        sum += factorial                  #int
    #output
    print('Сумма факториалов от 1 до', number, 'равна', sum)
