'''По данному натуральному n вычислите значение n!'''

#defaults
factorial = 1        #int

#input
print('Task 1: Нахождение факториала')
number = int(input('Введите натуральное число: '))  #int; число с фиксированной точкой

#main
if number < 0:
    print ('Введено недостимое значение!')
else:
    for i in range (1, number + 1):
        factorial *= i
    #output
    print(number,'! =',factorial)   #int
print ()
