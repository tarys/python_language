''' Дано число n. Создайте массив размером n×n и заполните его
по следующему правилу. На главной диагонали должны быть записаны числа 0.
На двух диагоналях, прилегающих к главной, числа 1.
На следующих двух диагоналях числа 2, и т.д.'''

n = int(input())
a = [[int(0)] * n for i in range(n)]
for i in range(n):
    for j in range(n):
        a[i][i] = 0
        if i < j:
            a[i][j] = j - i
        elif i > j:
            a[i][j] = i - j
for row in a:
    for elem in row:
        print(elem, sep = ' ', end = ' ')
    print()

