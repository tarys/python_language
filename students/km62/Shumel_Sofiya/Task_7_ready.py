list = [[int(j) for j in input().split()]for i in range(n)]

max_i = 0
max_j = 0
max_element = list[0][0]

for i in range(n):
    for j in range(m):
        if list[i][j] > max_element:
            max_element = list[i][j]
            max_i = i
