''' Дан двумерный массив и два числа: i и j. Поменяйте в массиве столбцы
с номерами i и j и выведите результат.

Программа получает на вход размеры массива n и m,
затем элементы массива, затем числа i и j.

Решение оформите в виде функции swap_columns(a, i, j).'''

def swap_columns(a, i, j):
    for k in range(len(a)):
        a[k][i], a[k][j] = a[k][j], a[k][i]
n, m = [int(s) for s in input().split()]
a = []
for i in range(n):
    a.append([int(s) for s in input().split()])
i, j = [int(s) for s in input().split()]
swap_columns(a, i, j)
for i in range(n):
    print(' '.join([str(a[i][j]) for j in range(m)]))

