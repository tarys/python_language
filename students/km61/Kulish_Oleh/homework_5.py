#програма яка обчислює певний вираз
def power(a, n) :#задання функції
    if n == 0 :#порівняння числа
        return 1#результат
    return a * power(a, n - 1)#обчислення вирвзу, результат
a = float(input())#введення числа
n = int(input())#введення числа
print(power(a, n))#виведення числа
