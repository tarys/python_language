#програма яка визначає число Фібуччі за номером
import math#імпорт бібліотеки
def fib(n):#задання функції
    sqrt5 = math.sqrt(5)#обчислення кореня числа
    phi = (sqrt5 + 1) / 2#обчислення виразу
    return int(phi ** n / sqrt5 + 0.5)#результат
n = int(input())#введення № числа
print(fib(n))#виведення числа
