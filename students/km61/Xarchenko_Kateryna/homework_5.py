"""

Даны четыре действительных числа: x1, y1, x2, y2. Напишите функцию distance(x1,
y1, x2, y2), вычисляющая расстояние между точкой (x1,y1) и (x2,y2). 
Считайте четыре действительных числа и выведите результат работы этой функции.
Если вы не знаете, как решить эту задачу, то вы, возможно, не изучали в школе 
теорему Пифагора. Попробуйте прочитать о ней на Википедии.
"""


# Програма для обчислення відстані між двома точками
# Присвоєння функції
def distance(x1, y1, x2, y2):
    # Обчислення функції
    dist = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
    # вивід значення функції на екран
    print(dist)


# ввід координат точок
x1 = float(input())
y1 = float(input())
x2 = float(input())
y2 = float(input())
# обчислення через функцію
distance(x1, y1, x2, y2)
