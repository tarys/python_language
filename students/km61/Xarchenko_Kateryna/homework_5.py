"""

Напишите функцию fib(n), которая по данному целому неотрицательному n 
возвращает n-e число Фибоначчи. В этой задаче нельзя использовать циклы — 
используйте рекурсию.
Во всех задачах считывайте входные данные через input() и выводите ответ через 
print().
"""


# Програма для запису числа фібоначі за номером
# присвоєння функції
def fib(n):
    # якщо номер дорівнює 0 то число фібоначі 0
    if n == 0:
        return 0
    # якщо номер дорівнює 1 то число фібоначі 1
    elif n == 1:
        return 1
    # підрахунок за допомогою рекуренсії
    else:
        return fib(n - 1) + fib(n - 2)


# вивід числа фібоначі
print(fib(int(input())))
