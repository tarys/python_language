"""

Потерянная карточка
Для настольной игры используются карточки с номерами от 1 до N. Одна карточка 
потерялась. Найдите ее, зная номера оставшихся карточек.
Дано число N, далее N − 1 номер оставшихся карточек (различные числа от 1 до 
N). Программа должна вывести номер потерянной карточки.
Для самых умных: массивами и аналогичными структурами данных пользоваться 
нельзя.
"""
# програма для знахождення загубленої картки
# n - кількість карток в коді
# sum-сума значень карток sumkar-кума значень карток що залишились
n = int(input())
sum = 0
sumkar = 0
# обчислення суми значень карток
for i in range(1, n + 1):
    sum += i
# обчислення суми значень карток що залишилось
for i in range(n - 1):
    kar = int(input())
    sumkar += kar
# вивід загубленої картки
print(sum - sumkar)

