"""

Сумма факториалов
По данному натуральном n вычислите сумму 1!+2!+3!+...+n!. В решении этой 
задачи можно использовать только один цикл. Пользоваться математической 
библиотекой math в этой задаче запрещено.
"""
# Програма для підрахунку суми факторіалів
# sum- стала для підрхунку суми 
# fakt- стала для підрахунку факторіала
sum = 0
fact = 1
for i in range(int(input())):
    # Обчислення факторіала
    fact *= (i + 1)
    sum += fact
# Виведення відповіді
print(sum)

